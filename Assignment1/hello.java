public class hello {
    public static void main(String[] args) {
        // Declare numeric variables
        int num1 = 10;
        int num2 = 5;
        int num3 = 2;
        
        // Perform arithmetic operations
        int sumResult = num1 + num2;
        int diffResult = num1 - num3;
        int prodResult = num2 * num3;
        double divResult = (double)num1 / num2;
        int modResult = num1 % num3;
        
        // Print results
        System.out.println("Sum: " + sumResult);
        System.out.println("Difference: " + diffResult);
        System.out.println("Product: " + prodResult);
        System.out.println("Division: " + divResult);
        System.out.println("Modulus: " + modResult);
        
        // Print Hello, World!
        System.out.println("Hello, World!");
    }
}
